import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const sourceRoot = process.env.HFLT_WECHAT_SOURCE || "/Users/huangfulongtao/Desktop/hflt公众号项目";
const sourceArticles = path.join(sourceRoot, "articles");
const sourceAssets = path.join(sourceRoot, "assets");
const docsWechat = path.join(repoRoot, "docs", "wechat");
const docsWechatAssets = path.join(repoRoot, "docs", "wechat-assets");
const sourceLinksPath = path.join(__dirname, "wechat-source-links.json");

const generatedNotice = "<!-- Generated by scripts/sync-wechat-articles.mjs. Do not edit manually. -->";

const readJson = (filePath) => {
  if (!fs.existsSync(filePath)) return {};
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
};

const ensureDir = (dirPath) => {
  fs.mkdirSync(dirPath, { recursive: true });
};

const resetDir = (dirPath) => {
  fs.rmSync(dirPath, { recursive: true, force: true });
  ensureDir(dirPath);
};

const walk = (dirPath) => {
  if (!fs.existsSync(dirPath)) return [];
  const entries = fs.readdirSync(dirPath, { withFileTypes: true });
  return entries.flatMap((entry) => {
    const fullPath = path.join(dirPath, entry.name);
    if (entry.isDirectory()) return walk(fullPath);
    return fullPath;
  });
};

const toPosix = (value) => value.split(path.sep).join("/");

const escapeYaml = (value) => String(value).replace(/"/g, '\\"');

const escapeHtml = (value) =>
  String(value)
    .replace(/&/g, "&amp;")
    .replace(/"/g, "&quot;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");

const stripMarkdown = (value) =>
  value
    .replace(/!\[[^\]]*\]\([^)]+\)/g, "")
    .replace(/\[[^\]]+\]\(([^)]+)\)/g, "$1")
    .replace(/[*_`>#-]/g, "")
    .replace(/\s+/g, " ")
    .trim();

const getTitle = (markdown, fallback) => {
  const match = markdown.match(/^#\s+(.+?)\s*$/m);
  return match ? match[1].trim() : fallback;
};

const getDescription = (markdown) => {
  const lines = markdown.split(/\r?\n/).map((line) => line.trim());
  const candidate =
    lines.find(
      (line) =>
        line &&
        !line.startsWith("#") &&
        !line.startsWith("!") &&
        !line.startsWith("- ") &&
        !line.startsWith(">") &&
        !line.startsWith("|") &&
        !line.startsWith("```")
    ) || "";
  const clean = stripMarkdown(candidate);
  return clean.length > 90 ? `${clean.slice(0, 90)}...` : clean;
};

const rewriteImages = (markdown, articlePath) =>
  markdown.replace(/!\[([^\]]*)\]\(([^)]+)\)/g, (match, alt, rawSrc) => {
    const src = rawSrc.trim();
    if (/^(https?:)?\/\//.test(src) || src.startsWith("data:")) return match;

    const [pathname, suffix = ""] = src.split(/(?=[?#])/);
    const sourceImage = path.resolve(path.dirname(articlePath), pathname);

    if (!fs.existsSync(sourceImage)) {
      console.warn(`Missing image referenced by ${articlePath}: ${src}`);
      return match;
    }

    const relativeToAssets = path.relative(sourceAssets, sourceImage);
    if (relativeToAssets.startsWith("..")) {
      console.warn(`Image is outside source assets, skipped copy: ${sourceImage}`);
      return match;
    }

    const destImage = path.join(docsWechatAssets, relativeToAssets);
    ensureDir(path.dirname(destImage));
    fs.copyFileSync(sourceImage, destImage);

    return `![${escapeHtml(alt)}](../../wechat-assets/${toPosix(relativeToAssets)}${suffix})`;
  });

const formatDate = (date) => `${date.slice(0, 4)}-${date.slice(4, 6)}-${date.slice(6, 8)}`;

const buildArticle = ({ articlePath, date, slug, sourceLinks }) => {
  const raw = fs.readFileSync(articlePath, "utf8");
  const title = getTitle(raw, slug);
  const description = getDescription(raw);
  const sourceKey = `${date}/${slug}.md`;
  const originalUrl = sourceLinks[sourceKey];
  const body = rewriteImages(
    raw.replaceAll("https://ydlongtao.netlify.app/#contact", "https://ydlongtao.github.io/Myblog/"),
    articlePath
  );
  const sourceRelative = toPosix(path.relative(sourceRoot, articlePath));

  const sourceLine = originalUrl
    ? `> 本文同步自“现智研”微信推文工作区。发布日期：${formatDate(date)}。 [查看公众号原文](${originalUrl})`
    : `> 本文同步自“现智研”微信推文工作区。发布日期：${formatDate(date)}。来源：\`${sourceRelative}\`。`;

  return `---
title: "${escapeYaml(title)}"
date: ${formatDate(date)}
description: "${escapeYaml(description)}"
category: 微信推文
source: "${escapeYaml(sourceRelative)}"
${originalUrl ? `originalUrl: "${escapeYaml(originalUrl)}"\n` : ""}---

${generatedNotice}

${sourceLine}

${body}
`;
};

const buildIndex = (articles) => {
  const list = articles
    .map(
      (article) =>
        `- ${article.dateText} · [${article.title}](./${article.date}/${article.slug}.md)  \n  ${article.description}`
    )
    .join("\n");

  return `---
title: 微信推文
description: 同步自现智研公众号项目的文章列表。
---

${generatedNotice}

# 微信推文

这里同步展示“现智研”公众号项目中已经整理完成的 Markdown 推文。内容由本地同步脚本生成，原始成稿仍保留在 \`hflt公众号项目/articles\`。

## 最新文章

${list}
`;
};

if (!fs.existsSync(sourceArticles)) {
  throw new Error(`Cannot find source articles directory: ${sourceArticles}`);
}

resetDir(docsWechat);
resetDir(docsWechatAssets);

const sourceLinks = readJson(sourceLinksPath);
const articlePaths = walk(sourceArticles)
  .filter((filePath) => filePath.endsWith(".md"))
  .sort();

const articles = articlePaths
  .map((articlePath) => {
    const date = path.basename(path.dirname(articlePath));
    const slug = path.basename(articlePath, ".md");
    if (!/^\d{8}$/.test(date)) return null;
    const raw = fs.readFileSync(articlePath, "utf8");
    return {
      articlePath,
      date,
      dateText: formatDate(date),
      slug,
      title: getTitle(raw, slug),
      description: getDescription(raw),
    };
  })
  .filter(Boolean)
  .sort((a, b) => `${b.date}/${b.slug}`.localeCompare(`${a.date}/${a.slug}`));

for (const article of articles) {
  const outDir = path.join(docsWechat, article.date);
  ensureDir(outDir);
  fs.writeFileSync(
    path.join(outDir, `${article.slug}.md`),
    buildArticle({ ...article, sourceLinks }),
    "utf8"
  );
}

fs.writeFileSync(path.join(docsWechat, "README.md"), buildIndex(articles), "utf8");

console.log(`Synced ${articles.length} WeChat articles from ${sourceArticles}`);
